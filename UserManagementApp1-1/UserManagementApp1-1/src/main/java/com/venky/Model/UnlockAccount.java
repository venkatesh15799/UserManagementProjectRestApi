package com.venky.Model;

public class UnlockAccount {
	
	private String emailId;
	private String tmpPwd;
	private String newPwd;
	private String conformPwd;
	
	
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getTmpPwd() {
		return tmpPwd;
	}
	public void setTmpPwd(String tmpPwd) {
		this.tmpPwd = tmpPwd;
	}
	public String getNewPwd() {
		return newPwd;
	}
	public void setNewPwd(String newPwd) {
		this.newPwd = newPwd;
	}
	public String getConformPwd() {
		return conformPwd;
	}
	public void setConformPwd(String conformPwd) {
		this.conformPwd = conformPwd;
	}
	
	
}
